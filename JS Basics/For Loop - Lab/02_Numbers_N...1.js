// Напишете функция, която получава положително число n, въведено от потребителя и печата числата от n до 1
// в обратен ред. Въведеното число n, винаги ще бъде по-голямо от 1.

function Nto1(input) {
    number = Number(input[0])
    for (let i = number; i >= 1; i--){
        console.log(i)
    }
}

